#!/bin/bash

# O condicional (if) é usado para o código caso uma condição seja verdadeira.
# Assim como em outras linguagens, ele possui if, elif e else.
# É necessário usar o 'then' depois do ';'.

if <CONDITION>; then
      <STATEMENT>
      ...
      <STATEMENT>
elif <CONDITION>; then
      <STATEMENT>
      ...
      <STATEMENT>
else
      <STATEMENT>
      ...
      <STATEMENT>
fi
